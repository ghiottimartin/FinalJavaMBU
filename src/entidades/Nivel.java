package entidades;

public class Nivel {
	private int id_nivel, nro_nivel ,experiencia_minima, puntos_totales;

	public int getId_nivel() {
		return id_nivel;
	}

	public void setId_nivel(int id_nivel) {
		this.id_nivel = id_nivel;
	}

	public int getNro_nivel() {
		return nro_nivel;
	}

	public void setNro_nivel(int nro_nivel) {
		this.nro_nivel = nro_nivel;
	}

	public int getExperiencia_minima() {
		return experiencia_minima;
	}

	public void setExperiencia_minima(int experiencia_minima) {
		this.experiencia_minima = experiencia_minima;
	}

	public int getPuntos_totales() {
		return puntos_totales;
	}

	public void setPuntos_totales(int puntos_totales) {
		this.puntos_totales = puntos_totales;
	}
}
